namespace SharedKernel.Infrastructure.Permissions.AuthService;
public enum Permission
{
    /// <summary>
    /// Permission to create a new user
    /// </summary>
    Create_User = 1,
    /// <summary>
    /// Permission to read user information
    /// </summary>
    Read_User = 2,
    /// <summary>
    /// Permission to update user information
    /// </summary>
    Update_User = 3,
    /// <summary>
    /// Permission to delete a user
    /// </summary>
    Delete_User = 4,

}
